// Write a switch statement that
// console logs witty statements for each
// day of the standard work week: Monday, Tuesday,
// Wednesday, Thursday, Friday
var day = 'Tuesday';

switch(day) {
  case 'Monday':
    console.log('Not again...');
    break;
  case 'Tuesday':
    console.log('Meh.');
    break;
  case 'Wednesday':
    console.log('Yay! Hump day!');
    break;
  case 'Thursday':
    console.log('Almost there...!');
    break;
  case 'Friday':
    console.log('TGI Friday!! Let\'s hava party!!');
    break;
}
